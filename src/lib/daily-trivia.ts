export function dailyTriviaCount(): number {
  return DAILY_TRIVIA_LIST.length;
}

export function todayInTokyoIsoDate(now: Date = new Date()): string {
  return new Intl.DateTimeFormat("sv-SE", {
    timeZone: "Asia/Tokyo",
    year: "numeric",
    month: "2-digit",
    day: "2-digit",
  }).format(now);
}

function hashString(input: string): number {
  let hash = 2166136261;
  for (const ch of input) {
    hash ^= ch.charCodeAt(0);
    hash = Math.imul(hash, 16777619);
  }
  return hash >>> 0;
}

export function pickDailyTrivia(date: string): string {
  const index = hashString(date) % DAILY_TRIVIA_LIST.length;
  return DAILY_TRIVIA_LIST[index] ?? DAILY_TRIVIA_LIST[0]!;
}

export function readDailyTriviaRecord(date: string = todayInTokyoIsoDate()): DailyTriviaRecord {
  return {
    trivia: pickDailyTrivia(date),
    date,
  };
}
